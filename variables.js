// Variables

// Js is dynamically typed language -> datatype is allocated dynamically.
let a;
console.log("The value of a is ", a);
console.log("The datatype of a is ", typeof a);

//assign data to a
a = 100;
console.log("The value of a is ", a);
console.log("The datatype of a is ", typeof a);

a = "hello";
console.log("The value of a is ", a);
console.log("The datatype of a is ", typeof a);

a = true;
console.log("The value of a is ", a);
console.log("The datatype of a is ", typeof a);

//In JS, Variables don't have types - Values have types